// CFileData [BEGIN] /////////////////////////////////////////////////////////////////////////////////////

bool CFileData::IsRootDirectory() const
{
	if ( IsDirectory() )
	{
		const TCHAR* rootMask = _T("..");

		int i = 0;
		while ( rootMask[i] && cFileName[i]==rootMask[i] )
			i++;

		return ( i != 0 && cFileName[i] == _T('\0') );
	}
	return false;
}
